    public void loadIdToCbox() {
        cbox2.removeAllItems();
        cbox3.removeAllItems();
        for (Book book : listbook) {
            cbox2.addItem(book.getId() + "");
        }
        for (Reader reader : listReader) {
            cbox3.addItem(reader.getId() + "");
        }
    }

    public void loadDataToTable() {
        listbook = new ArrayList<>();
        listReader = new ArrayList<>();  // neu khong khoi tao thi no se tra ve null
        listBookManager =new ArrayList<>();
        FileIO.readFromFile(listbook, File_Book); // doc du lieu tu file sach.txt 
        FileIO.readFromFile(listReader, File_READER);//doc du lieu tu file bd.txt
        FileIO.readFromFile(listBookManager, File_BOOKMANAGER);
        for (Book b : listbook) {
            tableBook.addRow(b.toObject());
        }
        for (Reader r : listReader) {
            tableReader.addRow(r.toObject());
        }
        for (BookManager bm : listBookManager) {
            tableBookManager.addRow(bm.toObject());
        }
    }

