    private void cbox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbox4ActionPerformed
       String option =cbox4.getItemAt(cbox4.getSelectedIndex());
       if(option.equals("sắp xếp theo tên BD")){
           Comparator<BookManager> cpt =(BookManager o1, BookManager o2) -> {
               String bookName1 =o1.getReader().getName();
               String bookName2 =o2.getReader().getName();
               return bookName1.compareTo(bookName2);
           };
           Collections.sort(listBookManager,cpt );
       }
       else{
           Collections.sort(listBookManager,Comparator.comparing(BookManager::getAmount).reversed());
       }
        tableBookManager.setRowCount(0);
        for (BookManager bm : listBookManager) {
            tableBookManager.addRow(bm.toObject());
        }
        
    }//GEN-LAST:event_cbox4ActionPerformed

