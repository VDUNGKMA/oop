    private void btn_Add2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Add2ActionPerformed
        String readerName = tf_readername.getText();
        String readerAddress = tf_readeraddress.getText();
        String readerPhone = tf_readerphone.getText();
        if (readerName.equals("") || readerAddress.equals("") || readerPhone.equals("")) {
            JOptionPane.showMessageDialog(null, "ban da nhap thieu thong tin");
            return;
        }
        int position = listReader.size() - 1;
        if (position != -1) {
            Reader.setCurrent_id(listReader.get(position).getId() + 1);
        }
        if (readerPhone.length() <= 9 || readerPhone.length() > 10 || !readerPhone.startsWith("0")) {
            JOptionPane.showMessageDialog(null, "so dien thoai khong hop le");
            return;
        }
        Reader reader = new Reader(readerName, readerAddress, readerPhone);
        for (int i = 0; i < listReader.size(); i++) {
            if (listReader.get(i).equals(reader)) {
                JOptionPane.showMessageDialog(null, "nhap trung thong tin");
                return;
            }
        }
        tableReader.addRow(reader.toObject());
        listReader.add(reader);
        loadIdToCbox();
    }//GEN-LAST:event_btn_Add2ActionPerformed

