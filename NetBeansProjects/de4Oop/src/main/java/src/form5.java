    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!svattt.isSelected() && !svmm.isSelected()) {
            JOptionPane.showMessageDialog(null, "chon loai sinh vien");
            return;
        } else {
            if (svattt.isSelected()) {
                if (KiemtrarongSVATTT() == true) {
                    JOptionPane.showMessageDialog(null, "chua nhap du thong tin");
                    return;
                } else {
                    try {
                        SinhvienATTTT at = new SinhvienATTTT(txtMasv.getText(), txtHoten.getText(), txtNgaysinh.getText(), Nam.isSelected() ? "Nam" : "Nu",
                                Float.parseFloat(txtDiemtb.getText()), Float.parseFloat(txtHocphi.getText())
                        );
                        FileWriter fw = new FileWriter("svat.dat", true);
                        BufferedWriter bw = new BufferedWriter(fw);
                        bw.append(at.masv + "$" + at.hoten + "$" + at.ngaysinh + "$" + at.gioitinh + "$" + at.dtb + "$" + at.getHocphi());
                        bw.newLine();
                        bw.close();
                        fw.close();
                        JOptionPane.showMessageDialog(null, "luu file svat thanh cong");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "luu file that bai");
                    }

                }
            } else {
                if (KiemtrarongSVMM() == true) {
                    JOptionPane.showMessageDialog(null, "chua nhap du thong tin");
                    return;
                } else {
                    try {
                        SinhvienMatma mm = new SinhvienMatma(txtMasv.getText(), txtHoten.getText(), txtNgaysinh.getText(), Nam.isSelected() ? "Nam" : "Nu", Float.parseFloat(txtDiemtb.getText()),
                                txtDonvi.getText(), Float.parseFloat(txtLuong.getText()));

                        FileWriter fw = new FileWriter("svmm.dat");
                        BufferedWriter bw = new BufferedWriter(fw);
                        bw.append(mm.masv + "$" + mm.hoten + "$" + mm.ngaysinh + "$" + mm.gioitinh + "$" + mm.dtb + "$" + mm.getDonvi() + "$" + mm.getLuong());
                        bw.newLine();
                        bw.close();
                        fw.close();
                        JOptionPane.showMessageDialog(null, "luu file svmm thanh cong");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null,"luu file that bai");
                    }
                }

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

