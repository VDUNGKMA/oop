    public Boolean KiemtrarongSVATTT() {
        if (txtHoten.getText().equals("") || txtMasv.getText().equals("") || txtDiemtb.getText().equals("")
                || txtHocphi.getText().equals("") || txtNgaysinh.getText().equals("")
                || (!Nam.isSelected() && !Nu.isSelected())) {
            return true;
        }
        return false;
    }

    public Boolean KiemtrarongSVMM() {
        if (txtHoten.getText().equals("") || txtMasv.getText().equals("") || txtDiemtb.getText().equals("")
                || txtDonvi.getText().equals("") || txtNgaysinh.getText().equals("") || txtLuong.getText().equals("")
                || (!Nam.isSelected() && !Nu.isSelected())) {
            return true;
        }
        return false;
    }
